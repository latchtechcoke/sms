                    <section id="font_size" class="section">
                        <h3>Responsive font sizes</h3>
                        <p>Bootstrap v4.3 ships with the option to enable responsive font sizes, allowing text to scale more naturally across device and viewport sizes. RFS can be enabled by changing the <code>$enable-responsive-font-sizes</code> Sass variable to true and recompiling Bootstrap.</p>
                        <p>To support RFS, we use a Sass mixin to replace our normal <code>font-size</code> properties. Responsive font sizes will be compiled into <code>calc()</code> functions with a mix of <code>rem</code> and viewport units to enable the responsive scaling behavior.</p>
